                            <article class="entry dashView h2-content-table">
								<h2 id="Questcequunprtimmobilier">Qu’est ce qu’un prêt immobilier</h2>
								<p>Vous souhaitez <strong>acheter un logement</strong> :</p>
								<ul>
									<li>Votre première maison ou appartement</li>
									<li>Un terrain pour construire la maison de vos rêves</li>
									<li>Investir dans l’immobilier</li>
								</ul>
								
								<p>Pour financer un tel achat, la plupart des personnes a recourt au prêt immobilier.</p>
								<p>Un crédit immobilier est un emprunt bancaire destiné à financer :</p>
								<ul>
									<li><strong>L’achat d’un bien immobilier</strong> (appartement ou maison) ainsi que les éventuels travaux de réparation, d’amélioration ou d’entretien de ce bien</li>
									<li><strong>L’achat d’un terrain destiné</strong> à la construction d’un logement</li>
									<li><strong>La construction d’un logement</strong></li>
								</ul>
								<p>Vous devez rembourser ce crédit durant une période définie, moyennant des intérêts&nbsp;qui ont pour objet de rémunérer l’organisme bancaire qui vous a prêté l’argent.</p>
								<div class="msg info">
									<h3>A noter</h3>
									<p>L’emprunt immobilier sert le plus souvent à <strong>financer un logement</strong> qui sera utilisé comme résidence principale ou secondaire, ou encore un logement qui sera ensuite loué à un tiers.</p>
								</div>
								<h2 id="Pourquoifaireunprtimmobilier">Pourquoi faire un prêt immobilier</h2>
								<p>Voici les principales raisons de contracter un prêt immobilier :</p>
								<ul class="like">
									<li><strong>Acheter votre logement aujourd’hui sans avoir à avancer les fonds</strong>. Parfois, il est même plus intéressant de contracter un crédit immobilier pour financer votre logement que d’utiliser vos fonds disponibles.</li>
									<li><strong>Remplacer le paiement du loyer par la mensualité de votre prêt</strong> en sachant que vous êtes le bénéficiaire de ce loyer puisque vous êtes le propriétaire de votre logement.</li>
									<li><strong>Pouvoir échelonner le paiement d’un investissement conséquent tout en conservant votre épargne</strong> pour d’autres dépenses, faire face à des imprévus ou bien investir dans d’autres placements lucratifs.</li>
								</ul>
								<h2 id="Commentfonctionneleprtimmobilier">Comment fonctionne le prêt immobilier</h2>
								<p>L’emprunt immobilier est un crédit bancaire avec les caractéristiques générales suivantes :</p>
								<ul>
									<li><strong>Un montant précis</strong> accordé en fonction du prix du bien immobilier et de votre situation (salaire, type de contrat, épargne existante…) notamment. Il n’y a pas de montant maximum.</li>
									<li><strong>Une durée de remboursement longue.</strong>&nbsp;En moyenne de 20 à 25 ans mais pouvant aller jusqu’à 30 à 35 ans.</li>
									<li><strong>Un taux d’intérêt </strong>défini par la banque. Il peut être fixe ou variable.</li>
									<li><strong>Un apport personnel</strong> qui représente une partie de l’argent en votre possession que vous allez investir dans votre achat immobilier. Il s’agit de votre épargne disponible (livret A, PEL…) et de vos économies. Certains établissements accordent des prêts immobiliers sans apport personnel mais la plupart exige un minimum. L’apport personnel se situe en général entre 10 et 30% du total de votre achat immobilier. Plus il est important, moins votre crédit maison vous coûtera.</li>
									<li><strong>Une garantie de prêt</strong>. L’organisme prêteur vous demandera des garanties dans le cas de défaut de remboursement. Les garanties les plus courantes sont l’hypothèque, le privilège de prêteur de deniers et la caution bancaire. Voir la partie Garanties d’un prêt immobilier &nbsp;pour en savoir plus.</li>
									<li><strong>Une assurance emprunteur</strong>. La banque exige généralement la souscription d’une assurance pour garantir le prêt contre les risques de décès ou d’invalidité. Vous pouvez souscrire une assurance auprès de l’assureur de votre choix. Voir la partie Assurance de prêt immobilier &nbsp;pour en savoir plus.</li>
									<li><strong>Des frais de dossier</strong> qui représentent le coût de constitution et d’analyse de votre dossier de demande de crédit. En général, il s’élève à 1% du capital emprunté.</li>
									<li><strong>Une mensualité </strong>dont le montant est établi dans le contrat de prêt. C’est la somme que vous allez devoir rembourser chaque mois auprès de l’organisme financier.</li>
								</ul>
								<div class="msg info">
									<h3>Conseils</h3>
									<p>Nous vous recommande de <strong>ne pas mettre toutes vos économies</strong> dans l’apport personnel mais de garder une épargne de précaution en cas d’imprévu. Si vous achetez un bien immobilier, il est fort probable que vous deviez faire face à des <strong>dépenses non prévues</strong> dans votre budget tel que des travaux ou réparations diverses.</p>
								</div>
								<h2 id="Garantiesduncrditimmobilier">Garanties d’un crédit immobilier</h2>
								<p>
									<div class="img-wrapper" style="float: right;"><img class="alignright wp-image-4876 size-thumbnail lazyloaded" src="../front/seguridad-300x300.png" width="150" height="150"></div>Afin de vous accorder un crédit immobilier, tout organisme financier exige une garantie contre le non paiement de vos mensualités.
								<p>Vous avez le choix entre <strong>trois types de garantie</strong> :</p>
								<ol>
									<li>L’hypothèque</li>
									<li>Le privilège de prêteur de deniers (PPD)</li>
									<li>La caution</li>
								</ol>
								<p>Cette garantie est <strong>obligatoire</strong> et entraîne des<strong> frais supplémentaires</strong>. Il faut donc choisir celle qui est plus adaptée à votre situation.</p>
								<h3>L’hypothèque</h3>
								<p>L’hypothèque est <strong>la plus courante</strong> des garanties choisies par les emprunteurs.</p>
								<p>Elle permet à la banque de saisir le logement, de le vendre, et de se rembourser grâce à la somme obtenue en cas de non-paiement des mensualités.</p>
								<p>L’hypothèque est obligatoirement rédigée par un <strong>notaire</strong> et doit être publié au service de publicité foncière.</p>
								<div class="msg info">
									<h3>Coût hypothèque</h3>
									<p>Cette garantie coûte environ <strong>2 % du montant</strong> du crédit immobilier. Il s’agit principalement des frais de notaire.</p>
								</div>
								<h3>Le privilège de prêteur de deniers (PPD)</h3>
								<p>
									<div class="img-wrapper" style="float: right;"><img class="alignright wp-image-4862 size-thumbnail lazyloaded" src="../front/aceptado-300x300.png" width="150" height="150"></div>Le privilège de prêteur de deniers est <strong>similaire à l’hypothèque</strong>.
								<p>Cette garantie doit être rédigée par un notaire et&nbsp;publiée au service de publicité foncière.</p>
								<p>Son <strong>coût est inférieur à celui de l’hypothèque</strong> et elle este exonéré de la taxe de publicité foncière.</p>
								<p>Le privilège de prêteur de deniers ne peut garantir que les prêts immobiliers finançant l’achat d’un <strong>logement déjà construit ou d’un terrain</strong>.</p>
								<p>Il ne peut donc pas garantir la partie du prêt qui finance la construction de la maison.</p>
								<h3>La caution bancaire</h3>
								<p>La caution bancaire a un <strong>fonctionnement différent</strong>.</p>
								<p>C’est aussi la <strong>garantie la moins chère</strong> car elle nécessite un simple écrit qui n’a pas obligation à être rédigé par un notaire.</p>
								<p>Un <strong>établissement spécialisé</strong> ou un tiers se porte garant pour vous en échange d’une cotisation (lorsqu’il s’agit d’une société spécialisée notamment).</p>
								<div class="msg info">
									<h3>Fonctionnement caution bancaire</h3>
									<p>Si vous ne pouvez pas rembourser vos mensualités, votre garant doit rembourser les sommes dues à votre place.</p>
								</div>
								<h2 id="Assurancedecrditimmobilier">Assurance de crédit immobilier</h2>
								<p>
									<div class="img-wrapper" style="float: right;"><img class="alignright wp-image-4949 size-thumbnail lazyloaded" src="../front/contrato2-300x300.png" width="150" height="150"></div>L’organisme de prêt immobilier exige généralement la <strong>souscription d’une assurance emprunteur</strong> pour garantir le crédit immobilier contre les <strong>risques de décès ou d’invalidité</strong>.
								<p>L’organisme vous proposera très certainement une offre d’assurance lors de la constitution de votre dossier de prêt bancaire immobilier.</p>
								<p>Vous n’êtes pas dans l’obligation de l’accepter et <strong>vous pouvez choisir une assurance</strong> chez la société d’assurance de votre choix.</p>
								<p>Vous pouvez aussi choisir de vous assurer également contre le risque de perte d’emploi. Cette garantie est facultative.</p>
								<div class="msg warning">
									<h3>A savoir</h3>
									<p>Depuis septembre 2010, l’établissement prêteur <strong>ne peut plus vous imposer son contrat d’assurance</strong> groupe. Vous pouvez donc souscrire un contrat individuel auprès de la compagnie d’assurance de votre choix. Cette assurance doit cependant avoir au moins le même niveau de garantie que celle proposée par votre établissement.</p>
								</div>
								<h2 id="Typesdeprtimmobilier">Types de prêt immobilier</h2>
								<p>
									<div class="img-wrapper" style="float: right;"><img class="alignright wp-image-3672 size-thumbnail lazyloaded" sizes="(max-width: 150px) 100vw, 150px" src="../front/red-300x300.png" width="150" height="150"></div>Vous avez le choix entre plusieurs <strong>types d’emprunt immobilier</strong> pour votre financement immobilier.
								<p>Dans cet article, nous vous avons présenté le crédit le plus courant : le prêt immobilier classique (prêt bancaire libre).</p>
								<p>Celui-ci vous permet de <strong>financer la totalité du prix d’achat</strong> de votre bien immobilier.</p>
								<p>Cependant, vous pouvez obtenir avec votre prêt bancaire immobilier des prêts complémentaires en fonction de :</p>
								<ol>
									<li><strong>Votre profil</strong> (revenus, situation familiale, etc.)</li>
									<li>Des <strong>caractéristiques du bien immobilier</strong> visé (zone, surface, etc.)</li>
								</ol>
								<p>Le ou les prêts auxquels vous pouvez prétendre <strong>dépend de la nature de votre acquisition</strong> (logement neuf ou ancien,&nbsp;résidence principale, secondaire ou investissement locatif) mais aussi de <strong>votre situation</strong>.</p>
								<p>La plupart de ces prêts dispose de <strong>conditions d’accès spécifiques</strong> et certains ne représentent qu’un complément au prêt bancaire classique.</p>
								<p>Voici les différents types de prêt immobilier :</p>
								<ul>
									<li>Prêt à taux zéro (PTZ)</li>
									<li>Prêt épargne logement (PEL)</li>
									<li>Prêt d’accession sociale (PAS)</li>
									<li>Prêt action logement</li>
									<li>Prêt conventionné</li>
									<li>Prêt fonctionnaire</li>
									<li>Prêt 0 % des collectivités locales</li>
								</ul>
								<h2 id="Conseilspourfaireunprtimmobilier">Conseils pour faire un prêt immobilier</h2>
								<p>
									<div class="img-wrapper" style="float: right;"><img class="alignright wp-image-4950 size-thumbnail lazyloadwait" src="../front/idea-300x300.png" sizes="(max-width: 150px) 100vw, 150px" width="150" height="150"></div>Faire un prêt immobilier requiert votre <strong>engagement sur le long terme</strong> et peut peser lourd dans votre budget mensuel.
								<p>C’est pourquoi, avant de souscrire un crédit maison, il est important que vous réfléchissiez et que vous preniez en compte les éléments ci-dessous.</p>
								<p>Souscrire un prêt bancaire immobilier, c’est <strong>s’endetter sur le long terme</strong>.</p>
								<p>Par conséquent, vous devez réfléchir avant de vous engager dans un tel contrat car :</p>
								<ul>
									<li>Vous devrez assurer le remboursement de vos mensualités pendant plusieurs dizaines d’années</li>
									<li>Même en cas d’imprévu financier ou de perte d’emploi</li>
								</ul>
								<p>Comme tout crédit, un prêt logement <strong>coûte de l’argent</strong>. Vous devrez notamment payer des frais d’intérêt, d’assurance, de dossier et de garantie.</p>
								<p>Afin d’obtenir le crédit immobilier le plus avantageux, Nous vous recommande de comparer les offres de crédit et d’utiliser le <strong>Taux Annuel Effectif Global</strong> (TAEG) comme critère de comparaison.</p>
								<p>Le TAEG inclue tous les frais du prêt bancaire immobilier.</p>
								<div class="msg info">
									<h3>Conseils</h3>
									<ul>
										<li>Afin de maximiser vos chances de rembourser votre crédit immobilier sans impact négatif sur votre vie quotidienne, <strong>déterminez votre capacité de remboursement mensuelle.</strong></li>
										<li>Veillez à ce que la mensualité de votre prêt <strong>ne dépasse pas 33% de vos revenus mensuels</strong>.</li>
										<li>Si vous avez déjà des crédits en cours, veillez à ce que la totalité de vos mensualités ne dépasse pas ce ratio.</li>
									</ul>
								</div>
								<h2 id="Inconvnientsducrditimmobilier">Inconvénients du crédit immobilier</h2>
								<p>Vous devez aussi être conscient des contraintes qu’un emprunt immobilier implique, notamment si votre achat vous servira de résidence principale :</p>
								<ul class="dislike">
									<li><strong>Difficulté de déménager</strong>&nbsp;car il n’est pas toujours facile de vendre un bien immobilier</li>
									<li><strong>Difficulté de changer de travail. </strong>Comme vous devez assurer le remboursement de vos mensualités, vous ne pourrez pas toujours être ouvert à toutes les opportunités qui s’offrent à vous.</li>
									<li><strong>Un budget plus rigide</strong>. Comme les mensualités doivent être impérativement payées, vous devrez probablement limiter certains loisirs.</li>
								</ul>
								<h2 id="Commentfaireuncrditimmobilier">Comment faire un crédit immobilier</h2>
								<p>Pour faire un crédit immobilier, vous devez suivre les étapes suivantes :</p>
								<ol>
									<li><strong>Faire des demandes</strong> une demande crédit auto sur notre site</li>
									<li><strong>Remplir les formulaires</strong> de demande de prêt logement</li>
									<li>Chaque organisme étudie votre demande et <strong>revient vers vous avec une offre</strong> (s’ils acceptent votre demande)</li>
									<li>Vous <strong>comparez les offres</strong> de crédit immobilier</li>
