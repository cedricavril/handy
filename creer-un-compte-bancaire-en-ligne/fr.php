                            <article class="entry dashView h2-content-table">
								<h2 id="Questcequuncomptebancaire">Qu’est ce qu’un compte bancaire</h2>
								<p>Le compte bancaire est un service fourni par votre banque vous permettant de déposer et de gérer votre argent.</p>
								<p>Vous pouvez en général déposer et retirer des fonds mais aussi utiliser les moyens de paiement associés à votre compte en banque.</p>
								<p>Vous pouvez ouvrir un compte bancaire chez :</p>
								<ul>
									<li>Une banque traditionnelle</li>
									<li>Une banque en ligne</li>
									<li>Une banque mobile ou «&nbsp;néobanque&nbsp;»</li>
								</ul>
								<div class="msg info">
									<h2 class="" id="Votredroitaucompte">Votre droit au compte</h2>
									<p>Toute personne majeur a le «&nbsp;droit au compte&nbsp;».</p>
									<p>C’est à dire que si elle ne dispose pas de compte bancaire de dépôt, elle a le droit à l’ouverture d’un tel compte avec un accès aux services de base dans l’établissement de son choix.</p>
								</div>
								<h2 id="Catgoriesdecomptebancaire">Catégories de compte bancaire</h2>
								<p>Il existe différentes catégories de compte bancaire selon la nature du compte ou selon le nombre de titulaires.</p>
								<h3>Catégories selon la nature du compte</h3>
								<h4>Le compte courant</h4>
								<p>Le compte courant est le compte le plus répandu.</p>
								<p>Il permet d’effectuer les opérations bancaires de base&nbsp;:</p>
								<ul>
									<li>Encaissements de paiements et salaires</li>
									<li>Prélèvements automatiques</li>
									<li>Virements</li>
								</ul>
								<p>Vous pouvez retirer partie ou totalités de vos fonds déposés à tout moment.</p>
								<p>Il est très souvent associé à des moyens de paiement tel que la carte bancaire ou le carnet de chèques. Vous pouvez aussi parfois bénéficier d’un découvert autorisé.</p>
								<p>Le compte courant ne génère aucun intérêt.</p>
								<p>Le compte courant est également appelé «&nbsp;compte de dépôt&nbsp;», «&nbsp;compte chèque&nbsp;» ou même «&nbsp;compte à vue&nbsp;».</p>
								<h4><div class="img-wrapper" style="float: right;"><img class="alignright wp-image-246 size-thumbnail lazyloaded" src="https://bpt-finances.com/front/piggy-bank-1-300x300.png" sizes="(max-width: 150px) 100vw, 150px" src="https://cdn.financer.com/fr/wp-content/uploads/sites/17/2017/07/piggy-bank-1-150x150.png" width="150" height="150"></div>Le compte épargne</h4>
								<p>Les fonds que vous déposez sur votre compte épargne sont rémunérés en fonction d’un taux d’intérêt.</p>
								<p>Vous avez des livrets réglementés et défiscalisés – Livret A, LDDS ou LEP par exemple – et des livrets fiscalisés.</p>
								<p>Vous ne pouvez pas bénéficier de moyens de paiement ou découvert autorisé associé.</p>
								<p>Le compte épargne est également appelé «&nbsp;compte sur livret&nbsp;».</p>
								<h4>Le compte à terme</h4>
								<p>L’argent que vous déposez sur votre compte à terme est immobilisé et non accessible pendant une durée définie (entre un mois et dix ans).</p>
								<p>Les fonds sont rémunérés en fonction du taux d’intérêt défini dans le contrat.</p>
								<p>Vous pouvez retirer vos fonds avant la date prévue, mais vous aurez des pénalités.</p>
								<h4>Le compte-titres</h4>
								<p>Le compte-titres permet l’achat et le dépôt de valeurs mobilières (actions boursières, parts de fonds de placement et de SICAV ou encore obligations). Les dividendes et plus-values perçus sont soumis à une imposition spécifique.</p>
								<h3>Catégories selon le nombre de titulaires</h3>
								<h4>Le compte individuel</h4>
								<p>Le compte individuel est le compte le plus commun.</p>
								<p>Une seule personne physique en est titulaire et est autorisée à y effectuer des opérations. Vous pouvez cependant signer une procuration en faveur d’un tiers&nbsp;ou avoir un tuteur.</p>
								<h4>Le compte joint</h4>
								<p>Le compte joint est le plus souvent ouvert par des couples mariés, pacsés ou en concubinage. Il est ouvert conjointement par deux personnes.</p>
								<p>Chaque cotitulaire peut réaliser seul des opérations sur ce compte et les éventuelles dettes générées engageront solidairement les deux cotitulaires.</p>
								<h4>Le compte indivis</h4>
								<p>Le compte indivis requiert la signature de chacun des cotitulaires du compte pour qu’une opération soit réalisée, sauf si un mandataire commun a été nommé.</p>
								<div class="msg boxed">
									<h2 class="" id="Anoter">A noter</h2>
									<p>Dans cet article, nous traiterons en priorité du compte bancaire le plus commun : le compte courant individuel.</p>
								</div>
								
								<h2 id="Commentouvriruncomptebancaire">Comment ouvrir un compte bancaire</h2>
								<h3>1. Conditions d’ouverture d’un compte</h3>
								<p>Voici les conditions d’ouverture d’un compte courant individuel :</p>
								<ol>
									<li>Vous devez être majeur (ou au moins 16 ans et en présence d’un représentant légal se portant caution)</li>
								</ol>
								<h3>2. Documents à fournir</h3>
								<p>Pour ouvrir un compte, vous devrez généralement fournir les documents suivants :</p>
								<ol>
									<li>Une pièce d’identité en cours de validité (carte national d’identité, passeport…)</li>
									<li>Un justificatif de domicile de moins de 3 mois (quittance de loyer, facture d’eau…)</li>
									<li>Un dépôt de signature (afin qu’elle puisse vérifier les chèques)</li>
								</ol>
								<div class="msg info">
									<h2 class="" id="Asavoir">A savoir</h2>
									<p>La banque a le droit de vous demander d’autres justificatifs tel que vos 3 derniers relevés bancaires afin de connaître votre profil ou bien vos 3 derniers bulletins de salaire s’il s’agit d’une ouverture de compte premium.</p>
								</div>
								<h3>3. Procédure d’ouverture de compte</h3>
								<p>Ouvrir un compte bancaire est en général assez simple.</p>
								<p>Vous devez :</p>
								<ol>
									<li>Faire votre demande auprès de la banque de votre choix</li>
									<li>Fournir les documents nécessaires</li>
									<li>Signer le contrat</li>
								</ol>
								<h3>4. Acceptation ou refus de la banque</h3>
								<p>La banque est libre d’accepter ou de refuser votre demande d’ouverture de compte sans se justifier.</p>
								<p>Elle doit cependant vous informer&nbsp;de votre possibilité de saisir la Banque pour exercer votre droit au compte.</p>
								<div class="msg warning">
									<h2 class="" id="Vrificationsdevotrepassbancaire">Vérifications de votre passé bancaire</h2>
									<p>Sachez qu’après vérification de votre identité, la banque effectue des recherches sur votre passé bancaire.</p>
									<p>Elle interrogera notamment le fichier central des chèques (FCC) pour savoir si vous êtes interdit bancaire, et le fichier des incidents de remboursement des crédits aux particuliers (FICP) pour savoir si vous êtes en surendettement.</p>
									<p>Si vous choisissez une banque mobile (ou néobanque), celle-ci ne vérifiera pas votre passé bancaire.</p>
								</div>
								<h2 id="Commentfonctionneuncomptebancaire">Comment fonctionne un compte bancaire</h2>
								<p>
									<div class="img-wrapper" style="float: right;"><img class="alignright wp-image-254 size-thumbnail lazyloadwait" data-src="https://cdn.financer.com/fr/wp-content/uploads/sites/17/2017/07/presentation-5-150x150.png" alt="compte bancaire" srcset="" data-srcset="https://cdn.financer.com/fr/wp-content/uploads/sites/17/2017/07/presentation-5-150x150.png 150w, https://cdn.financer.com/fr/wp-content/uploads/sites/17/2017/07/presentation-5-300x300.png 300w, https://cdn.financer.com/fr/wp-content/uploads/sites/17/2017/07/presentation-5.png 512w" sizes="(max-width: 150px) 100vw, 150px" width="150" height="150"></div>Chaque catégorie de compte bancaire (voir partie plus haut) a ses spécificités et fonctionne différemment. Nous allons ici expliquer le fonctionnement d’un compte courant individuel.
								<p>Lorsque vous ouvrez un compte courant auprès d’une banque, cette dernière vous informera de toutes les conditions d’utilisation et de gestion mais aussi de tous les services bancaires qui y sont associés.</p>
								<p>De plus, suivant la banque et le compte ouvert, vous aurez accès aux services bancaires associés.</p>
								<p>Un compte courant vous permet de :</p>
								<ul>
									<li>Déposer des fonds à la banque</li>
									<li>Recevoir de l’argent de la part de tiers</li>
									<li>Procéder à des paiements</li>
								</ul>
								<p>&nbsp;</p>
								<p>Toutes les informations liées à votre compte courant (services, frais, conditions…) sont disponibles dans la convention de compte qui vous sera remis à l’ouverture de votre compte.</p>
								<div class="msg boxed">
									<h2 class="" id="Servicesbancairesduncomptecourant">Services bancaires d’un compte courant</h2>
									<p>Voici les services bancaires les plus communs auxquels vous aurez droit si vous ouvrez un compte courant :</p>
									<ul>
										<li>Détention d’un RIB (relevé d’identité bancaire)</li>
										<li>Dépôts, retraits et virements</li>
										<li>Moyens de paiement associés au compte tel qu’un chéquier ou une <a href="#cartes/">carte bancaire</a>. L’obtention d’une carte bancaire peut nécessiter le paiement d’une cotisation supplémentaire suivant le modèle choisi.</li>
										<li>Consulter et gérer votre compte à distance (espace client en ligne)</li>
										<li>Relevé mensuel bancaire</li>
										<li>Donner procuration à une autre personne</li>
										<li>Possibilité d’obtenir d’un découvert autorisé</li>
									</ul>
								</div>
								<h2 id="Combiencoteuncomptebancaire">Combien coûte un compte bancaire</h2>
								<p>Tout comme les services liés à votre compte, les frais bancaires varient d’une banque à une autre et d’un compte à un autre.</p>
								<p>Les banques en ligne ont généralement des frais plus faibles que les banques traditionnelles.</p>
								<p>Les banques mobiles proposent généralement une version gratuite avec compte courant et carte offerte.</p>
