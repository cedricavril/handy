								<h2 id="Choisirlacarteadaptevosbesoins">Choisir la carte adaptée à vos besoins</h2>
								<p>
							  <div class="img-wrapper" style="float: right;"><img class="alignright wp-image-3559 size-full lazyloaded" src="../front/if_checkmark_1891021-150x150.png" width="150" height="150"></div>
								
								<p>Il existe actuellement de nombreuses cartes de paiement sur le marché. Chaque carte a des fonctionnalités particulières ainsi que des avantages et des inconvénients.</p>
								<p>Voici les <strong>3 principales catégories</strong> :</p>
								<ol>
									<li>Cartes de crédit</li>
									<li>Cartes prépayées</li>
									<li>Cartes de débit</li>
								</ol>
								
								<h2 id="Diffrencesentrecatgories">Différences entre catégories</h2>
								<p>Voici un tableau récapitulatif vous expliquant les principales différences entre les différentes catégories de cartes : cartes de crédit, prépayées et de débit.</p>
								<table>
									<tbody>
										<tr>
											<th>Type de carte</th>
											<th>Carte de crédit</th>
											<th>Carte prépayée</th>
											<th>Carte de débit</th>
										</tr>
										<tr>
											<td><strong>Est-elle liée à un compte bancaire ?</strong></td>
											<td>Oui</td>
											<td>Non, il s’agit d’un compte en ligne dans lequel vous ajoutez le montant de votre choix</td>
											<td>Oui</td>
										</tr>
										<tr>
											<td><strong>D’où vient l’argent dépensé ?</strong></td>
											<td>Votre banque qui vous prête de l’argent</td>
											<td>L’argent disponible dans votre compte en ligne</td>
											<td>L’argent disponible sur votre compte bancaire</td>
										</tr>
										<tr>
											<td><strong>Pouvez-vous dépenser plus d’argent que vous n’en avez ?</strong></td>
											<td>Oui, l’argent vous prête de l’argent que vous remboursez les mois suivants</td>
											<td>Non, aucun découvert autorisé</td>
											<td>Non, mais possibilité de découvert autorisé</td>
										</tr>
										<tr>
											<td><strong>Pourquoi utiliser cette carte ?</strong></td>
											<td>Période de difficultés financières ou de plus grandes dépenses</td>
											<td>Gestion facile et maîtrisée de votre argent</td>
