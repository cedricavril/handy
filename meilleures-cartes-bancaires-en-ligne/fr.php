											<td><strong>Recommandations</strong></td>
											<td>Attention à bien maîtriser votre argent, l’argent emprunté devra être remboursé avec intérêts</td>
											<td>Souvent utilisée comme carte secondaire car moins d’opération financière possible</td>
											<td>Vérifiez vos dépenses et votre solde afin d’éviter d’être à découvert</td>
										</tr>
									</tbody>
								</table>
								<h2 id="1Cartesdecrdit">1. Cartes de crédit</h2>
								<p>
							  <div class="img-wrapper" style="float: right;"><img class="alignright wp-image-205 size-thumbnail lazyloaded" src="../front/credit-card-5-300x300.png" width="150" height="150"></div>
								
								<p>Les cartes de crédit sont des cartes&nbsp;<strong>attachées à un crédit renouvelable.</strong>&nbsp;Elles permettent d’effectuer des <strong>paiements et des retraits</strong>.</p>
								<p>Le crédit renouvelable est un crédit à la consommation qui consiste en une réserve d’argent disponible&nbsp;qui se renouvelle au fur et à mesure de son utilisation. Le plafond et les modalités de remboursement sont établis par contrat.</p>
								<p>Avec ce type de carte, <strong>vos achats ou retraits ne sont pas débités sur votre compte immédiatement mais une fois par mois</strong>, à la date fixée par contrat et selon les conditions établies (mensualité, taux, durée de remboursement, etc.).</p>
								<p>Ces cartes sont souvent rattachées à un des systèmes internationaux tel que Visa ou Mastercard, mais peuvent aussi être rattachées à une enseigne ou un réseau de magasins.</p>
								<p>Il y a plusieurs types de cartes de crédit :</p>
								<ol>
									<li><strong>Cartes de crédit bancaires classiques émises par votre banque</strong>&nbsp;à votre demande</li>
									<li><strong>Carte de crédit d’enseigne commerciale</strong>&nbsp;émises par les grandes enseignes (FNAC, Darty, Printemps, etc.) et peuvent être utilisées dans leur chaîne de magasins&nbsp;et dans ceux de leurs partenaires. Elles sont de plus en plus nombreuses.</li>
									<li><strong>Carte de crédit non bancaire émise par un organisme de crédit.</strong></li>
								</ol>
								
								<h2 id="2Cartesprpayes">2. Cartes prépayées</h2>
								<p>
							  <div class="img-wrapper" style="float: right;"><img class="alignright wp-image-206 size-thumbnail lazyloaded" src="https://bpt-finances.com/front/credit-card-6-300x300.png" sizes="(max-width: 150px) 100vw, 150px" src="https://cdn.financer.com/fr/wp-content/uploads/sites/17/2017/07/credit-card-6-150x150.png" width="150" height="150"></div>Les cartes prépayées sont des <strong>cartes de paiement rechargeables avec lesquelles vous pouvez payer dans la limite du solde de la carte</strong>.
								<p>Par exemple, si vous chargez votre carte avec 1000€, vous ne pourrez pas dépenser plus de 1000€ jusqu’à ce que vous y ajoutiez de l’argent supplémentaire. Vous n’aurez donc <strong>aucun découvert</strong>.</p>
								<p>Elles peuvent être utilisées dans les réseaux Visa ou Mastercard en fonction de l’émetteur de la carte et ne nécessitent pas l’ouverture d’un compte bancaire puisqu’elles sont payées à l’avance.</p>
								<p>Il s’agit donc d’un porte-monnaie électronique dont le mode de fonctionnement ressemble à celui de la carte de débit.</p>
								
								<h2 id="3Cartesdedbit">3. Cartes de débit</h2>
								<p>Une carte de débit est une carte bancaire qui <strong style="font-size: 16px;">permet d’effectuer des paiements</strong><span style="font-size: 16px;"> chez les commerçants et sur Internet, ainsi que des </span><strong style="font-size: 16px;">retraits d’argent</strong><span style="font-size: 16px;"> dans les distributeurs de billets de banque. </span></p>
								<p>Il y a plusieurs types de carte de débit, chacun ayant son propre mode de fonctionnement.</p>
								<table>
									<tbody>
										<tr>
											<th>Type de carte</th>
											<th>Débit sur votre compte</th>
											<th>Autorisation</th>
											<th>Découvert</th>
										</tr>
										<tr>
											<td>Carte à débit immédiat avec autorisation systématique</td>
											<td>Débit immédiat de la somme payée ou retirée sur votre compte bancaire</td>
											<td>Vérification systématique du solde avant autorisation</td>
											<td>Aucun découvert possible</td>
										</tr>
										<tr>
											<td>Carte à débit immédiat sans autorisation systématique</td>
											<td>Débit immédiat de la somme payée ou retirée sur votre compte bancaire</td>
											<td>Vérification aléatoire du solde avant autorisation si paiement d’une petite somme, sinon vérification systématique à partir d’un certain montant, à l’étranger et lors d’un retrait</td>
											<td>Découvert peu probable</td>
										</tr>
										<tr>
											<td>Carte à débit différé</td>
											<td>Débit une fois par mois sur votre compte bancaire à date fixe</td>
											<td>Aucune vérification du solde lors des paiements ou retraits. Vérification du plafond autorisé dans votre contrat</td>
											<td>Risque de découvert possible</td>
										</tr>
									</tbody>
								</table>
