								<h2 id="Questcequelecrditenlignerapide">Qu’est ce que le crédit en ligne rapide ?</h2>
								<p>
									<div class="img-wrapper" style="float: right;"><img class="alignright wp-image-6244 size-thumbnail lazyloaded" alt="credit rapide en ligne 24h" src="../front/money-bag-150x150.png" width="150" height="150"></div>Le crédit rapide en ligne est le meilleur moyen d’obtenir de l’argent rapidement.
								<p>Il permet de faire face à des dépenses imprévues ou à des fins de mois difficiles.</p>
								<p>Il s’agit en quelque sorte d’une <strong>aide financière d’urgence</strong>.</p>
								<p>C’est un prêt personnel de courte durée et de faible montant.</p>
								<p>Vous pouvez généralement emprunter <strong>entre 50€ et 1000€</strong>. Le délai de remboursement est lui aussi très court : <strong>de 15 jours à 1 mois</strong>.</p>
								<p>Le crédit rapide est en général très <strong>facile et rapide à obtenir</strong>.</p>
								<p>La demande se fait directement sur Internet avec très peu de formalités administratives.</p>
								<p>Comme le prêt personnel, vous n’avez&nbsp;<strong>aucun justificatif d’utilisation&nbsp;</strong>à fournir. Vous êtes libre d’utiliser l’argent emprunté comme vous le souhaitez.</p>
								<div class="msg info">
									<h2 class="" id="Anoter">A noter</h2>
									<p>Plusieurs termes sont employés pour définir ce type de crédit : crédit rapide, mini crédit, crédit express, crédit urgent, mini prêt et parfois <a href="#credit/micro/">microcrédit</a> (qui n’est pourtant pas un terme approprié).</p>
								</div>
								<h2 id="Quanddemanderunminicrdit">Quand demander un mini crédit ?</h2>
								<p>Vous l’avez compris, un crédit rapide est généralement contracté quand on a un <strong>besoin urgent d’argent</strong>.</p>
								<p>Ci-dessous, les cas dans lesquels faire une demande de crédit rapide est adapté et les cas dans lesquels il ne l’est pas.</p>
								
								<p>Voici d’autres <strong>cas courant de demande</strong> de crédit rapide :</p>
								<ul>
									<li>Régler une formation professionnelle</li>
									<li>S’installer dans son nouvel appartement</li>
									<li>Réparer sa moto</li>
									<li>Vous offrir un bien «&nbsp;coup de coeur&nbsp;»</li>
									<li>Régler des frais de santé</li>
									<li>Avance sur salaire : vous avez besoin de liquidités mais vous n’avez pas encore reçu votre salaire. Vous pourrez finir le mois en toute tranquillité</li>
								</ul>
								
								<h2 id="Crditrapidesous24haexiste">Crédit rapide sous 24h : ça existe ?</h2>
								<p>
									<div class="img-wrapper" style="float: right;"></div>C’est une question que beaucoup se demandent : le crédit en 24h sans justificatif existe-t-il ?
								<p>La réponse est : <strong>OUI</strong>.</p>
								<p>Mais sous condition.</p>
								<p>Vous pouvez obtenir un <strong>crédit en 24h</strong> (après acceptation de votre dossier) seulement si le montant de votre emprunt est <strong>inférieur à 200€</strong>.</p>
								<p>Si vous empruntez plus de 200€, le <strong>délai habituel de 7 jours</strong> s’applique.</p>
								<p>Pourquoi 200€ ?</p>
								<p>C’est le plafond établi par la loi.</p>
								<p>En effet, la <a href="#loi-lagarde-credit-consommation/">Loi Lagarde de 2010</a> a imposé des mesures pour protéger les consommateurs contre les abus de certains établissements financiers.</p>
								<p>L’article L312-25 de ladite loi précise que durant 7 jours à compter de la date de l’acceptation de l’offre de crédit, aucun paiement ne peut vous être fait par l’organisme de crédit.</p>
								<p>Seuls les mini prêts dont le montant est inférieur à 200€ peuvent être accordés sous 24h.</p>
								<div class="msg warning">
									<h2 class="" id="Asavoir">A savoir</h2>
									<p>Les organismes de crédit n’ont donc pas le droit de vous accorder un crédit en 24h si son montant est supérieur à 200€. Par conséquent, n’acceptez aucune offre qui vous propose plus de 200€ en moins de 24h. <strong>Méfiez-vous</strong>, c’est certainement une arnaque !</p>
								</div>
								<div class="msg faq-box">
									<h2 class="faq_inner" id="FAQsurlecrditrapide">FAQ sur le crédit rapide</h2>
									<h4>Peut-on faire un crédit rapide en ligne en 24h ?</h4>
									<div class="inner">
										<p class="faq_inner">Oui, si vous empruntez <strong>moins de 200€</strong>, vous pouvez recevoir votre argent sous 24h. Si vous empruntez plus de 200€, le délai habituel de 7 jours s’applique.</p>
									</div>
									<h4>Combien peut-on emprunter avec le mini crédit ?</h4>
									<div class="inner">
										<p class="faq_inner">Vous pouvez emprunter <strong>entre 50€ et 1000€</strong> avec un délai de remboursement de 15 jours à 1 mois.</p>
									</div>
									<h4>Comment faire une demande de crédit rapide en ligne ?</h4>
									<div class="inner">
										<p class="faq_inner">Vous devez suivre la procédure :</p>
										<ol class="faq_inner">
											<li><strong>Remplir le formulaire de demande</strong> en ligne. C’est très facile et rapide.</li>
											<li><strong>Recevoir une réponse de l’organisme</strong> par email ou sms.</li>
											<li><strong>Obtenir l’argent emprunté</strong> sur votre compte.</li>
										</ol>
									</div>
									<h4>Le mini crédit est-il accessible aux interdits bancaires ?</h4>
									<div class="inner">
										<p class="faq_inner">Oui, le crédit rapide est ouvert aux personnes fichées à la Banque et aux interdits bancaires.</p>
									</div>
									<h4>On m’a parlé du crédit rapide sans justificatif, ça existe ?</h4>
									<div class="inner">
										<p class="faq_inner">Cela signifie que vous êtes <strong>libre d’utiliser les fonds empruntés</strong> comme vous le souhaitez. Comme c’est un mini prêt vous aurez moins de justificatifs à fournir mais vous devrez tout de même confirmer votre identité.</p>
									</div>
									<h4>J’ai moins de 18 ans. Ai-je le droit d’emprunter ?</h4>
									<div class="inner">
										<p class="faq_inner">Non, vous devez être majeur pour faire avoir le droit de contracter un crédit.</p>
									</div>
									
								</div>
								<h2 id="Avantagesduminicrditenligne">Avantages du mini crédit en ligne</h2>
								<p>Voici les principaux avantages du crédit rapide :</p>
								<ul class="like">
									<li>Faites votre <strong>demande de crédit en ligne</strong>.</li>
									<li><strong>Rapide à obtenir</strong>, entre 24h et quelques jours suivant le montant et votre dossier.</li>
									<li><strong>Simple à obtenir.</strong> Peu de justificatifs à fournir ou&nbsp;de formalités administratives.</li>
									<li>Aucun frais de dossier.&nbsp;<strong>Pas de dossier papier à envoyer</strong>, tout se fait en ligne.</li>
									<li><strong>Accessible à tous</strong>, même aux interdits bancaires personnes fichées</li>
								</ul>
								<h2 id="Estceuncrditsansjustificatif">Est-ce un crédit sans justificatif ?</h2>
								<p>
									<div class="img-wrapper" style="float: right;"></div>On parle souvent de mini crédit en 24h sans justificatif ou bien de crédit en ligne rapide <strong>sans justificatif de salaire</strong>.
								<p>C’est surtout pour dire que vous n’avez <strong>pas besoin de justifier l’utilisation</strong> des fonds empruntés.</p>
								<p>Aussi, comme le montant du mini prêt est faible, vous avez très peu de justificatifs à fournir, contrairement au crédit traditionnel.</p>
								<p>Malgré tout, le crédit rapide en ligne reste un prêt à la consommation. Il est donc nécessaire de constituer un dossier de prêt.</p>
								<p>En revanche, vous aurez bien <strong>moins de justificatifs à fournir</strong>.</p>
								<p>On vous demandera généralement les informations suivantes :</p>
								<ul>
									<li><strong>Votre pièce d’identité</strong> – Passeport ou carte nationale d’identité</li>
									<li><strong>Votre numéro d’IBAN</strong> figurant sur votre relevé d’identité bancaire (RIB).</li>
									<li><strong>Les informations de votre carte bancaire</strong>. Les remboursements de votre prêt se feront automatiquement via votre carte bancaire.</li>
								</ul>
