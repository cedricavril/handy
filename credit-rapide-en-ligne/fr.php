								<p>Pour obtenir un crédit rapide, voici les <strong>conditions requises </strong>:</p>
								<ol>
									<li>Avoir plus de 21 ans</li>
									<li>Disposer d’un revenu mensuel</li>
								</ol>
								<p>Pour obtenir votre crédit rapide, il vous suffit de faire une<strong> demande en ligne</strong>. Suite à cette demande, l’établissement vous donnera une réponse de principe dans les heures qui suivent.</p>
								<p>Suivant le montant, vous obtiendrez une réponse définitive en 24h ou quelques jours après analyse de votre dossier.</p>
								<p>Il existe deux façons de rembourser votre crédit rapide en ligne :</p>
								<ol>
									<li><strong>Par virement bancaire</strong></li>
									<li><strong>Par carte bancaire</strong></li>
								</ol>
									
								<h2 id="Crditconsorapidepourinterditbancaire">Crédit conso rapide pour interdit bancaire</h2>
								<p>Comme les montants empruntés sont faibles et les durées de remboursements très courtes, les établissements spécialisés dans les mini crédits sont bien <strong>moins strictes sur les conditions d’emprunt</strong>.</p>
								<p>Le crédit rapide est donc <strong>accessible&nbsp;aux personnes fichées</strong> à la Banque  et interdits bancaires.</p>
								<h2 id="Faireunedemandedecrditrapide">Faire une demande de crédit rapide</h2>
								<p>La procédure d’obtention d’un crédit en ligne rapide est très simple :</p>
								<ol>
									<li><strong>Remplissez le formulaire de demande</strong> en ligne en quelques clics.</li>
									<li>Vous obtenez une réponse de principe en quelques minutes voir quelques heures (hors weekends et jours fériés)</li>
									<li><strong>Envoyez la documentation</strong> par email (quelques justificatifs)</li>
									<li><strong>Recevez votre argent sous 24h</strong> si vous empruntez moins de 200€ et jusqu’à 7 jours si vous empruntez plus de 200€.</li>
								</ol>
								<h2 id="Miniprtscurisettransparent">Mini prêt : sécurisé et transparent</h2>
								<p>
									<div class="img-wrapper" style="float: right;"></div>La vitesse de gestion du prêt n’est pas incompatible avec la sécurité.
								<p>Si vous faites votre demande de crédit rapide , vous disposez d’une sécurité égale à celle d’une banque traditionnelle.</p>
								<p>Lorsque vous faites votre demande sur Internet, toute <strong>la procédure est sécurisée</strong>.</p>
								<p>Toutes les données personnelles que vous transmettez sont des <strong>données privées et entièrement protégées</strong>.</p>
								<p>Les transactions bancaires sont réalisées de manière sécurisées.</p>
								
