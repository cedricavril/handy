									<h2 class="" id="Exempleillustratif">Exemple illustratif</h2>
									<ul>
										<li>Céline et Thibaud, un jeune couple, souhaite acheter un canapé convertible pour leur nouvel appartement. Ils décident de faire appel au crédit renouvelable pour financer le bien. Ils peuvent faire la demande <strong>soit directement dans le magasin</strong> où ils souhaitent acheter le canapé <strong>soit auprès d’un organisme financier en ligne</strong>.</li>
										<li>Le couple opte pour le crédit renouvelable en ligne sans justificatif car ils ont d’autres projets en tête qu’ils souhaiteraient financer dans un futur proche. L’établissement leur propose un crédit renouvelable de 2000€ au TAEG (Taux Annuel Effectif Global) de 19%.</li>
										<li>Pour financer leur canapé, Céline et Thibaud décident d’utiliser 1000€ sur les 2000€ mis à leur disposition grâce à leur carte de crédit. Les <strong>intérêts à payer</strong> se calculent uniquement sur la somme utilisée. Le couple détermine avec leur organisme financier qu’il remboursera 70€ par mois. S’ils n’utilisent pas le reste de leur <strong>réserve d’argent</strong>, Céline et Thibaud rembourseront 70€ par mois pendant 16 mois, plus une 17ème mensualité de 8€.</li>
										<li>6 mois plus tard, le couple souhaitent acheter un home cinéma. Ils ont de nouveau besoin de 1000€. A ce moment-là, ils ont déjà remboursé 286€ sur les 1000€ utilisés pour leur canapé. La <strong>somme d’argent disponible</strong> est alors de 1 286€ et le capital restant dû de 714€.</li>
										<li>Ils décident une fois de plus d’utiliser 1000€ sur leur crédit renouvelable en ligne. Le couple conserve leur mensualité à 70€. Ils devront donc rembourser 30 mensualités de 70€ ainsi qu’une dernière de 32€.</li>
										<li>Céline et Thibaud ont <strong>financé deux projets avec un seul crédit renouvelable</strong>. Ils ont été libre d’acheter ce qu’ils souhaitaient avec leur carte de crédit liée à leur réserve d’argent et n’ont pas eu à justifier leurs achats auprès de leur organisme de crédit.</li>
									</ul>
